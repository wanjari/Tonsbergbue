<legend class="text-left"> <h2>Om oss</h2></legend>
<br/>
<p> Tønsberg og Omegn Bueskyttere ble stiftet 4. mai 1965 av en håndfull interesserte under et møte som ble holdt i Tønsberg Handelsstands Forenings lokaler.</p>
<p> Det står i klubbens protokoller at initiativtakere til å få startet en bueskytterklubb i Tønsberg, var Egil Bruno Larsen, Sportsklubben Vidar, daværende president i Norges Bueskytterforbund, og Arvid Johansen fra Lillestrøm, formann i NBF’s PR-komite. </p>
<p> Vi har hatt vår egen utendørs treningsbane siden 1966. Banen ligger på Auli, ved Jarlsberg Travbane, og området er leid av Jarlsberg Hovedgård.</p>
<p> Klubben er faktisk veldig heldig som har flotte lokaliteter vi disponerer, både innendørs og utendørs.</p>
<br/>
<p> På vinterstid, når vi trener inne, holder vi til i kjelleren i Tønsberg idrettshall. Her har vi stort lokale med plass til ca 20 skyttere samtidig på 
skytelinjen! Vi har mulighet å skyte på alle avstander opp til 18 meter. I tillegg har vi et sidelokale hvor vi har mulighet til å skyte opp til 25 meter. 
Her er det bare plass til 2-3 skyttere samtidig. I kjelleren har vi bueskytterutstyr som nye medlemmer/prøve-skyttere kan benytte. 
Det er både buer, piler og beskyttelsesutstyr. Buene er forskjellige slik at det skal være mulighet for alle å kunne prøve en bue som passer for dem. 
Vi har også en liten krok med sofaer og bord og stoler hvor vi sitter og koser oss med kaffe og prat. Det er en meget hyggelig atmosfære i klubben.</p>
<br/>
<p>På sommerstid når vi trener ute, har vi et eget område ved Auli (vi parkerer ved siden av Gravdals Auto). På andre siden av jernbanelinja ligger banen vår. 
Her har vi også et eget klubbhus med lager i ene enden og stue med kjøkkenkrok i den andre enden. Her har vi fast oppsatte skytematter på avstandene 10m, 20m, 30m, 40m, 50m, 60m, 70m og 90m. 
I tillegg har vi noen skytematter som vi kan sette hvor vi vil, for å skyte på andre avstander enn disse.  
Også her er det mulig for nybegynnere og prøve-skyttere å komme og låne utstyr. </p>
<br/>
<p> Klubben har et sterkt ønske om å få flere medlemmer. Derfor oppfordrer vi deg til å ta en tur innom oss, ta en prat og prøve denne flotte sporten. <p>
